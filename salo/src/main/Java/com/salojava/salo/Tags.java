public class Tags {
    private int TID;
    private String TName;
    private String TCata;
    private int TPCount;
    private int TSCount;

    public int getTID() {
        return TID;
    }

    public void setTID(int TID) {
        this.TID = TID;
    }

    public String getTName() {
        return TName;
    }

    public void setTName(String TName) {
        this.TName = TName;
    }

    public String getTCata() {
        return TCata;
    }

    public void setTCata(String TCata) {
        this.TCata = TCata;
    }

    public int getTPCount() {
        return TPCount;
    }

    public void setTPCount(int TPCount) {
        this.TPCount = TPCount;
    }

    public int getTSCount() {
        return TSCount;
    }

    public void setTSCount(int TSCount) {
        this.TSCount = TSCount;
    }
}
